# Distance Converter

miles = int(input("What is the distance in miles?"))

dFeet = miles * 5280
dYard = miles * 1760
dInches = miles * 63360

print("The distance is feet is = " + str(dFeet))
print("The distance is yard is = " + str(dYard))
print("The distance is inches is = " + str(dInches))