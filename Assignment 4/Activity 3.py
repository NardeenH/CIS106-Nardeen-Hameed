# Distance Converter

miles = int(input("What is the distance in miles?"))

d_Feet = miles * 5280
d_Yard = miles * 1760
d_Inches = miles * 63360

print("The distance is feet is = " + str(d_Feet))
print("The distance is yard is = " + str(d_Yard))
print("The distance is inches is = " + str(d_Inches))

